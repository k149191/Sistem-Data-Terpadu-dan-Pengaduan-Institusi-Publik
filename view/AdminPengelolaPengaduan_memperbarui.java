    private void loadIdTanggapanToCombo() {
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        model.addElement("-- Pilih ID Tanggapan --");

        final String sql = "SELECT id_tanggapan FROM tanggapan ORDER BY id_tanggapan DESC";
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {

            while (rs.next()) {
                model.addElement(rs.getString("id_tanggapan"));
            }

        } catch (SQLException ex) {
            logger.log(java.util.logging.Level.SEVERE, "Gagal memuat id_tanggapan", ex);
            JOptionPane.showMessageDialog(this, "Gagal memuat ID Tanggapan: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
        jComboBox2.setModel(model);
        jComboBox2.setSelectedIndex(0);
    }

    private void loadTanggapanById(String id) {
        final String sql = "SELECT status_keluhan, isi_tanggapan, tanggal_tanggapan FROM tanggapan WHERE id_tanggapan = ?";
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, id);
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    String status = rs.getString("status_keluhan");
                    String isi    = rs.getString("isi_tanggapan");
                    Timestamp ts  = rs.getTimestamp("tanggal_tanggapan");

                    // set ke field
                    if (status == null) status = "ditindaklanjuti";
                    jComboBox1.setSelectedItem(status);
                    jTextArea1.setText(isi != null ? isi : "");
                    jTextField2.setText(ts != null ? SHOW_DT.format(ts.toLocalDateTime()) : "");
                } else {
                    JOptionPane.showMessageDialog(this, "ID Tanggapan tidak ditemukan.");
                    resetFormDetail();
                }
            }
        } catch (SQLException ex) {
            logger.log(java.util.logging.Level.SEVERE, "Gagal memuat tanggapan", ex);
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + ex.getMessage());
        }
    }

    private void perbaruiTanggapan() {
        if (jComboBox2.getSelectedIndex() <= 0) {
            JOptionPane.showMessageDialog(this, "Pilih ID Tanggapan terlebih dahulu.");
            return;
        }
        String id      = (String) jComboBox2.getSelectedItem();
        String status  = (String) jComboBox1.getSelectedItem();
        String isi     = jTextArea1.getText().trim();
        String tglStr  = jTextField2.getText().trim();

        if (status == null || status.isEmpty() || isi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Status dan Isi Tanggapan wajib diisi.");
            return;
        }

        LocalDateTime ldt;
        try {
            if (tglStr.isEmpty()) {
                ldt = LocalDateTime.now();
            } else {
                ldt = LocalDateTime.parse(normalizeInputDateTime(tglStr), INPUT_DT);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Format tanggal tidak valid. Gunakan: yyyy-MM-dd atau yyyy-MM-dd HH:mm");
            return;
        }

        final String sql = "UPDATE tanggapan " +
                           "SET status_keluhan = ?, isi_tanggapan = ?, tanggal_tanggapan = ? " +
                           "WHERE id_tanggapan = ?";

        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setString(1, status);
            ps.setString(2, isi);
            ps.setTimestamp(3, Timestamp.valueOf(ldt));
            ps.setString(4, id);

            int updated = ps.executeUpdate();
            if (updated == 0) {
                JOptionPane.showMessageDialog(this, "ID Tanggapan tidak ditemukan. Tidak ada data diperbarui.");
                return;
            }

            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui.\nWaktu: " + SHOW_DT.format(ldt));

            AdminPengelolaPengaduan_melihat lihat = new AdminPengelolaPengaduan_melihat();
            lihat.setVisible(true);
            this.dispose();

        } catch (SQLException ex) {
            logger.log(java.util.logging.Level.SEVERE, "Gagal memperbarui tanggapan", ex);
            JOptionPane.showMessageDialog(this, "Gagal memperbarui: " + ex.getMessage());
        }
    }

    private String normalizeInputDateTime(String raw) {
        String s = raw.trim();
        if (s.matches("\\d{4}-\\d{2}-\\d{2}$")) s = s + " 00:00";
        return s;
    }

    private void resetFormDetail() {
        jComboBox1.setSelectedIndex(0);
        jTextArea1.setText("");
        jTextField2.setText("");
    }
    
