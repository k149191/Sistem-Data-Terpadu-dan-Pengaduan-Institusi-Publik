    private void loadDataAnggaran() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);

    String sql = 
        "SELECT a.id_anggaran, i.nama_instansi, a.tahun, a.nama_program, " +
        "       a.total_anggaran, a.realisasi, a.keterangan " +
        "FROM anggaran a " +
        "JOIN instansi i ON i.id_instansi = a.id_instansi " +
        "ORDER BY a.id_anggaran DESC";

    try (Connection conn = database.DatabaseConnection.getConnection();
         PreparedStatement ps = conn.prepareStatement(sql);
         ResultSet rs = ps.executeQuery()) {

        while (rs.next()) {
            Object[] row = new Object[] {
                rs.getString("nama_instansi"),
                rs.getString("tahun"),
                rs.getString("nama_program"),
                rs.getBigDecimal("total_anggaran"),
                rs.getBigDecimal("realisasi"),
                rs.getString("keterangan")
            };
            model.addRow(row);
        }
    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
    }
}
